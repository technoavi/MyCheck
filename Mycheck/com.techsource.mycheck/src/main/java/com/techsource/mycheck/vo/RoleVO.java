package com.techsource.mycheck.vo;

public class RoleVO {

	private int id;
	private String name;

	public RoleVO() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
